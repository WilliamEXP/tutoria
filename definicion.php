    <h2>Definición de conceptos</h2>

    <p>El adecuado empleo de las diversas acepciones empleadas en términos de la Tutoría, permite tener un lenguaje claro y asegura la sinergia de las acciones que permiten el logro de los objetivos del programa. Estos conceptos han sido establecidos por el Comité Académico de la Institución de forma colegiada y se presentan a continuación para la adecuada comprensión del documento.</p>

    <h3>Comité de Tutorías.</h3> 

<p>Cuerpo académico encargado de revisar los productos que ofrece la acción Tutorial y toma decisiones respecto de dichos productos, de forma que incidan positivamente en la eficiencia terminal y el rendimiento académico, disminuyendo la reprobación y el abandono escolar.</p> 



<h3>Coordinador Institucional de Tutoría.</h3>

<p>Autoridad escolar que se encarga de administrar todas las funciones y actividades relacionadas con la acción Tutorial. El perfil del Coordinador resulta relevante ya que debe ser capaz de realizar diagnósticos no solo académicos sino conductuales y de personalidad.</p>

<h3>Guía del Tutor.</h3>

<p>Documento Institucional que contiene la información necesaria para que el Tutor desarrolle las actividades correspondientes a la Tutoría grupal.</p>

<h3>Tutela.</h3>

<p>Autoridad y cargo de Tutor que incluye la instrucción destinada a la guarda de la persona y es ejercida por un Tutor.</p>


<h3>Tutor(a).</h3>

<p>Persona que ejerce la tutela, es el profesor encargado de la función Tutorial. Los principales deberes de un Tutor son derivados de las necesidades del estudiante a su cargo así como rendir cuentas de su gestión.</p>

<h3>Tutorado(a).</h3>

<p>Persona que recibe la Tutoría y que representa a todo estudiante inscrito en el Instituto Tecnológico.</p>

<h3>Tutoría.</h3>

<p>Función del Tutor que hace referencia al proceso de: orientación, guía y asistencia escolar. La Tutoría tiene una connotación más académica, se trata de orientar y ayudar a un estudiante o grupo de estudiantes en sus actividades relacionadas con el aprendizaje, ayudarles en la resolución de tareas y ofrecerles las facilidades para encontrar información de manera oportuna y rápida.
Asesoría Académica.</p>

<p>Estrategia Institucional que permite gestionar espacios y recursos humanos para formar de manera extracurricular a un estudiante o un grupo de ellos, puedan reforzar sus conocimientos académicos, habilidades, destrezas y competencias, que permitan contribuir a mejorar su rendimiento académico.</p>

<h3>Baja Académica.</h3>

<p>Proceso académico que permite a un estudiante manifestar su deseo de no continuar temporal o definitivamente con sus estudios. También se refiere al proceso que sigue la Institución para no seguir considerando a un estudiante como alumno regular por alguna violación al reglamento académico.</p>

       
