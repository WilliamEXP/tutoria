    <h2>Formatos de Trabajo</h2>
    <p>El comité de Tutorías ha diseñado diversos instrumentos orientados a registrar la evidencia indispensable para medir el progreso de la acción tutorial, así como evaluarla y tomar decisiones pertinentes. En la plataforma de tutorías se encuentran a disposición los formatos vacíos y con ejemplos para ilustrar mejor su empleo. Los formatos incluidos son el de: Tutoría individual, Tutoría grupal, de seguimiento, informe semestral de Tutoría, solicitud de asesorías, reporte de asesoría.</p>
        <p>
        El formato de Tutoría individual permite registrar las entrevistas que derivan de la Tutoría personalizada y es un instrumento valioso como una memoria histórica que se incluye en el expediente del alumno con la finalidad de incorporarlo a la evidencia de su trayectoria escolar.</p>
        





    <p>El formato de Tutoría grupal permite registrar las observaciones hechas en sesiones grupales de Tutoría y constituye un documento en el que quedan plasmados acuerdos que se tomen en la sesión, así como las actividades que aborden problemáticas específicas del grupo. La Guía del Tutor contempla actividades que el Tutor deberá desarrollar en las sesiones grupales. Sin embargo, en ocasiones, es necesario que el Tutor convoque a sesiones grupales extraordinarias para comunicar algo o para tomar acuerdos, como resultado de esas sesiones extraordinarias se empleará este formato invariablemente.</p>
    <p>El registro de seguimiento, enmarca el desarrollo del proceso de la Tutoría y permite dar seguimiento y rastreabilidad que puede integrarse a la trayectoria escolar del estudiante para tomar decisiones más pertinentes sobre el apoyo que requiera cada estudiante en particular.
El formato de informe semestral de Tutoría es un documento que registra en resumen las incidencias en materia tutorial del grupo y permite contar con un mecanismo de evaluación de la tarea del Tutor a la vez que muestra los principales indicadores de progreso académico y de formación de los alumnos.</p>
    <p>El formato de solicitud de asesorías le permite al Tutor realizar la gestión para ofrecer a los estudiantes que lo solicitan y lo requieran, asesorías académicas impartidas por especialistas del área de formación.</p>
    <p>El reporte de asesoría le permite al Tutor registrar los resultados de la asesoría que ha sido solicitada e impartida a los estudiantes.</p>
    <p>La Guía del Tutor es un compendio de actividades que deberá seguir el Tutor en cada sesión grupal ordinaria, que contempla todos los semestres de Tutoría grupal del alumno y ha sido creada tomando como punto de partida los procesos de formación más importantes que atraviesa el estudiante en el Tecnológico.</p>

       
