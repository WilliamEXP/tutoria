                <p>2.1 Grafique la curva de tu vida en el espacio.
                    Dibuje una línea que represente su vida
                    desde la infancia hasta el día de hoy,
                    mostrando los puntos altos y bajos. El eje
                    vertical representa el nivel de felicidad, 
                    satisfacción y realización. El eje horizontal 
                    representa tu edad al momento de los 
                    eventos significativos y los puntos de
                    inflexión.</p>

