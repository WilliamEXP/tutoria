            <h2>Semana 2 | LA ESCUCHA ACTIVA</h2>

            <h3>Descripción</h3>
            <p>
            Reflexionar sobre la importancia de saber escuchar a las personas, para establecer una comunicación efectiva.

            </p>

            <h3>Pasos a seguir:</h3>

            <ol>
                <li>
                El Tutor comenzará la sesión relatando a sus estudiantes la siguiente historia:
                <B>La bibliotecaria experta de escucha activa.</B>
                <P>
                En una biblioteca dos usuarios están peleando entre ellos:
                uno quiere abrir la ventana porque le falta el aire y el otro
                quiere que quede cerrada porque tiene reuma y teme a la
                corriente. Levantan la voz y molestan a los otros lectores.
                Una primera bibliotecaria llega: “¡Silencio! ¡Si no se
                sientan y se callan, los hago salir a ambos!”. Los dos se
                sientan, pero después de un rato vuelven a pelear. Una
                segunda bibliotecaria llega, escucha cuidadosamente a
                ambos y luego, después de una breve reflexión, propone:
                ¿Qué les parece si abrimos la ventana de la habitación
                más cercana, de modo que el aire circule sin provocar
                corriente?
                </P>
                </li>
                <li>El Tutor pedirá a los estudiantes que reflexionen
                    sobre esta historia que acaban de escuchar. Y
                    después de escuchar a los estudiantes les
                    explicará las diferentes formas de proceder de las
                    personas.</li>
                    <li>
                    Aquí se presentan tres modos de proceder:
                        <ul>
                            <li>3.1. El primero es argumentativo, porque la
                            ventana tiene que quedar abierta, cerrada, o
                            abierta a medias, etc.,</li>
                            <li>3.2. El segundo es de procedimiento, el
                            reglamento impone silencio y tiene que ser
                            respetado, </li>
                            <li>3.3. El tercero está basado en la escucha activa,
                            es decir, asume que ambos contendientes
                            tienen razón y busca una solución creativa
                            que sirva a ambos.</li>

                        </ul>
                    </li>
                    <li>El Tutor presentará a los estudiantes una definición
                    de Escucha Activa y pedirá que den sus opiniones
                    sobre este tema.
                    Pregunta: ¿Qué es la escucha activa?
                    <p>
                    "Si quieres comprender lo que otro está diciendo, tienes
                    que asumir que tiene razón, y preguntarle para que te
                    ayude a ver las cosas y los acontecimientos desde su
                    perspectiva. La escucha Activa implica el paso de una
                    actitud del tipo "justo-equivocado", "yo tengo razón – tú te
                    equivocas, o viceversa-, "amigo-enemigo", "verdaderofalso",
                    "normal-anormal", a otro en que se asume que el
                    interlocutor es inteligente y que hace falta meterse en sus
                    condiciones, para entender cómo es y qué
                    comportamientos y acciones (que nos pueden parecer
                    irrazonables y/o que nos molestan o irritan), para el otro
                    son totalmente razonables y racionales.
                    </p>
                    </li>
                    <li>El Tutor indicará a los estudiantes que efectúen la
                    actividad correspondiente a la escucha activa que
                    se presenta en el siguiente vínculo:
                    <p>
                    <a href=http://biblioteca.itson.mx/oa/desarrollo_personal/oa5/escucha_eficaz/index.htm>http://biblioteca.itson.mx/oa/desarrollo_personal/oa5/escucha_eficaz/index.htm</a>
                    </p>
                    </li>
                    <li>El Tutor presentará a los alumnos una selección de
                    diferentes videos disponibles en el material que
                    profundizan en el tema, presentando al menos dos
                    de estos en la sesión, o de manera alterna formará
                    equipos de trabajo para que cada equipo comente
                    el video un video diferente.</li>
                    <li>El Tutor cerrará la sesión de escucha activa
                    haciendo una reflexión sobre los temas vistos y la
                    importancia para la vida personal y académica de
                    los estudiantes.</li>

            </ol>
            
            
