                <li>El Tutor procederá a presentar el video <a href="https://drive.google.com/file/d/1i11MYnvTIpDuZcDxz3aeOBcTeYvx0KiX/view?usp=sharing">Búsqueda de información</a> y/o <a href="https://drive.google.com/file/d/1_cWvVvNulFjB5IlQYVnwGt4ODXTJGIBh/view?usp=sharing">los 4 pasos de la búsqueda de la información</a>. Disponible en el Material.</li>
