            <h2>Semana 6 | IDENTIDAD CON EL PROGRAMA EDUCATIVO</h2>

            <h3>Descripción</h3>
            <p>Reflexión acerca de la importancia de la Identidad con el
            Programa Educativo mediante un ejercicio de F.O.D.A.
            (Fortalezas, Oportunidades, Debilidades y Amenazas).</p>

            <h3>Pasos a seguir:</h3>

            <ol type ="1">
                <li>Se recomienda al Tutor leer el texto “FODA”
                (fortalezas, oportunidades, debilidades y
                amenazas). Disponible en el material.</li>
                <li>El Tutor explicara a los alumnos la Técnica FODA.</li>
                <li>El Tutor pedirá que en una hoja en blanco dividan
                un cuadrante en cuatro partes iguales. Cada
                cuadrante será nombrado como a) Fortalezas, b)
                Oportunidades, c) Debilidades y d) Amenazas.</li>
                <li>De manera personal y a través de una reflexión
                personal los estudiantes realizarán un FODA
                referente a la identidad con el programa educativo.</li>
                <li>Se sugiere que el Tutor ejemplifique con lo
                siguiente:</li>
                <p><u>Fortalezas</u>
                ¿Qué talentos especiales consideras relucir referente al
                programa educativo? Identifica cuáles son tus mayores
                talentos. (Pueden ser habilidades o competencias).
                ¿Qué es lo que la gente más admira de ti? (cualidades y
                virtudes).
                ¿Cuáles son tus experiencias más valiosas? (experiencias
                personales, relaciones).</p> 
                <p><u>Debilidades</u>
                ¿Cuáles es la debilidad o deficiencia más común que
                consideras tener dentro del programa educativo?
                (fracasos).
                ¿Qué es lo que más te gustaría cambiar en el próximo
                semestre?</p>
                <p><u>Amenazas</u>
                En un futuro ¿cuáles crees que es el reto más grande que
                tendrás que enfrentar?
                ¿Qué es lo que con mayor frecuencia evita lo que
                actualmente no puedes enfrentar?
                ¿A qué le tienes miedo?</p>
                <p><u>Oportunidades</u>
                ¿Qué nuevas oportunidades y posibilidades de mejora
                parecen presentarse ahora? (pueden ser amistades,
                eventos o sucesos.
                Cuando piensas en el futuro, ¿Qué es lo que más te
                entusiasma?
                ¿Qué harías si no tuvieras miedo?</p>
                <li>Finalmente el Tutor invita a 2 o 3 estudiantes a
                compartir el ejercicio de reflexión.</li>
            </ol>
